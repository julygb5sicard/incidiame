# Modulus with addition
result_add = 10 % 3
print(f"10 % 3 = {result_add}")  # Output: 10 % 3 = 1

# Modulus with multiplication
result_mult = (7 * 8) % 5
print(f"(7 * 8) % 5 = {result_mult}")  # Output: (7 * 8) % 5 = 1

# Modulus with subtraction
result_sub = 15 - (7 % 3)
print(f"15 - (7 % 3) = {result_sub}")  # Output: 15 - (7 % 3) = 14
